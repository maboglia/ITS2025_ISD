package scarabocchi;




public class Tabelline {

	public static void main(String[] args) {
		
		final int RIGHE = 10, COLONNE = 10;
		
		
		for (int i = 1; i <= RIGHE; i++) {
			
			for (int j = 1; j <= COLONNE; j++) {
				
				System.out.print( i * j + "\t");
				
			}
			
			System.out.println();
			
			
		}
		
		
		
		
		
		
		
	}
	
	

}
